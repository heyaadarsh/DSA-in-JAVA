package Operators;

public class arithmeticOperators {
    public static void main(String args[]){
        int a = 20;
        int b = 10;

        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;
        int rem = a % b;


        System.out.println("Sum of the numbers are: " + sum);
        System.out.println("Sub of the numbers are: "+ sub);
        System.out.println("Mul of the numbers are: "+ mul);
        System.out.println("Div of the numbers are: "+ div);
        System.out.println("Rem of the numbers are: "+ rem);
    }
}
