package Operators;

public class incrementOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        int p = 10;
        int q = p++;
        System.out.println(p);
        System.out.println(q);
    }
}
